    private void filterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterActionPerformed
        // TODO add your handling code here:
        java.util.Date tanggalFilter = tanggal_filter.getDate();
        if (tanggalFilter != null) {
            // Konversi tanggal ke format yang cocok dengan database (misal: yyyy-MM-dd)
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tanggalFilterStr = sdf.format(tanggalFilter);

            // Hapus data tabel sebelumnya
            daftartabel.setRowCount(0);

            // Query untuk mengambil data berdasarkan tanggal filter
            try {
                sql = "SELECT * FROM tbl_log WHERE tanggal = '" + tanggalFilterStr + "' ORDER BY id_log DESC";
                rs = stat.executeQuery(sql);
                while (rs.next()) {
                    daftartabel.addRow(new Object[]{
                        rs.getInt("id_log"),
                        rs.getString("waktu"),
                        rs.getString("tanggal"),
                        rs.getString("aktivitas"),
                        rs.getString("tipe_user"),
                        rs.getString("username"),
                        rs.getInt("id_user"),
                    });
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            // Kasih tau user kalau belum pilih tanggal
            JOptionPane.showMessageDialog(null, "Pilih tanggal dulu, ya!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_filterActionPerformed

