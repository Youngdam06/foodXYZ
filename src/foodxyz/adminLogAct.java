    public void listLog() {
        daftartabel = new DefaultTableModel();
        daftartabel.addColumn("id");
        daftartabel.addColumn("waktu");
        daftartabel.addColumn("tanggal");
        daftartabel.addColumn("aktivitas");
        daftartabel.addColumn("tipe user");
        daftartabel.addColumn("username");
        daftartabel.addColumn("id_user");
        list.setModel(daftartabel);
        FoodXYZ list = new FoodXYZ();
        list.konfig(); 
        Connection kon = list.getConnection(); 
        try{
            stat = kon.createStatement();
            sql = "SELECT * FROM tbl_log ORDER BY id_log DESC";
            rs = stat.executeQuery(sql);
            while(rs.next()){
                daftartabel.addRow(new Object[]{
                    rs.getInt("id_log"),
                    rs.getString("waktu"),
                    rs.getString("tanggal"),
                    rs.getString("aktivitas"),
                    rs.getString("tipe_user"),
                    rs.getString("username"),
                    rs.getInt("id_user"),
                });
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    

